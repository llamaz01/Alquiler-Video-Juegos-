    /**
     * Este metodo se encarga de agregar los datos cargados a un jtable y lo
     * retorna
     *
     * @return
     */
    public DefaultTableModel listaJPlay() {
        String c[] = {"Titulo", "Descripción", "Categoria", "Monto"};
        DefaultTableModel tablet = new DefaultTableModel(c, 0);
        try {
            BufferedReader buffer = new BufferedReader(new FileReader("juegosPlay.txt"));
            String p;
            while ((p = buffer.readLine()) != null) {

                StringTokenizer datos = new StringTokenizer(p, "|");

                Vector vector = new Vector();
                while (datos.hasMoreTokens()) {
                    vector.addElement(datos.nextToken());
                }
                tablet.addRow(vector);

            }
        } catch (IOException ex) {
            System.out.println("Error" + ex.getLocalizedMessage());
        }
        return tablet;
    }

