/**
     * Este metodo se encarga de pedir el nombre del juego que desea alquilar y
     * pregunta si es que existe ese juego digitado dentro de la base de datos
     *
     * @param evt
     */
    private void btnAlquilarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlquilarMouseClicked
        String juego = " ";
        try {
            juego = JOptionPane.showInputDialog("Digite el nombre del juego que desea comprar" + "\n➫ Obs: sea claro con el nombre del juego!").toLowerCase();

        } catch (Exception e) {
            //pequeños errores 
        }
        if (!play.existeDentroDeLaBase(juego)) {
            String monto = play.dameMonto(juego);
            String jugar = juego;
            AlquilerJuegos compra = new AlquilerJuegos(jugar, monto);
            compra.setTitulo(jugar);
            compra.setMonto(monto);
            compra.alquilarJuego(compra);
            cliente.setNombre(mdosCliente.buscarNombre(mdosCliente.ultimoRegitro()));
            cliente.setApellido(mdosCliente.buscarApellido(mdosCliente.ultimoRegitro()));
            cliente.setCedula(mdosCliente.ultimoRegitro());
            cliente.setDireccion(mdosCliente.buscarDomicilio(mdosCliente.ultimoRegitro()));
            mdosCliente.guardarCompra(cliente);
            JOptionPane.showMessageDialog(null, "➫ Procedimiento realizado exitosamente!", "", 0, add.dimensionarImagen("src/Iconos/Aok.png", 70, 70));

        } else if (juego != null && juego != " ") {
            JOptionPane.showMessageDialog(null, "➫ El nombre del juego no fue introducido correctamente", "", 0, add.dimensionarImagen("src/ImagenJuegos/error.png", 70, 70));
        }
    }//GEN-LAST:event_btnAlquilarMouseClicked
