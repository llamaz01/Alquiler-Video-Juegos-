/**
 * Metodo que devuelve el monto dependiendo de la categoria que se encuentre
 * @return 
 */
    public String monto() {
        String monto = "";
        if (jLabel3.getText().equals("PC")) {
            monto = "25";
        }
        if (jLabel3.getText().equals("XBOX")) {
            monto = "40";
        }
        if (jLabel3.getText().equals("WII")) {
            monto = "35";
        }
        if (jLabel3.getText().equals("PLAY")) {
            monto = "30";
        }
        return monto;
    }

