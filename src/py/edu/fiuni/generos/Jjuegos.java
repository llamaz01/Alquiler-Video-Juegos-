/**
 *Evento que se encarga de verificar si el juego ya a sido alquilado o no
 * para poder proceder a alquilarlo y agregar a la base de datos
 * @param evt 
 */
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        if (compra.existeJuego(jLabel2.getText())) {

            int n = JOptionPane.showConfirmDialog(
                    null, "Deseas alquilar este juego?", "", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.YES_OPTION) {
                compra.setTitulo(jLabel2.getText());
                compra.setMonto(this.monto());
                compra.alquilarJuego(compra);
                //agregamos el cliente dentro del informe
                cliente.setNombre(mdosCliente.buscarNombre(mdosCliente.ultimoRegitro()));
                cliente.setApellido(mdosCliente.buscarApellido(mdosCliente.ultimoRegitro()));
                cliente.setCedula(mdosCliente.ultimoRegitro());
                cliente.setDireccion(mdosCliente.buscarDomicilio(mdosCliente.ultimoRegitro()));
                mdosCliente.guardarCompra(cliente);

                this.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "El juego ya a sido alquilado!!");
        }

    }//GEN-LAST:event_jButton1MouseClicked
